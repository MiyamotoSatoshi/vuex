<template>
  <v-row>
    <v-col cols="12">
      <div class="d-flex">
        <app-text-field
          v-model="search"
          :append-inner-icon="view ? 'mdi-view-grid-outline' : 'mdi-view-list-outline'"
          :placeholder="placeholder"
          @click:append-inner.stop.prevent="view = !view"
        />
      </div>
    </v-col>

    <v-col
      v-for="job in items"
      :key="job.id"
      cols="12"
      :md="view ? 6 : undefined"
    >
      <v-card
        :href="job.url"
        border
        class="transition-swing"
        max-height="225"
        rel="sponsored"
        target="_blank"
        variant="flat"
        @click="onClick(job)"
      >
        <v-list-item
          :title="job.title"
          lines="two"
        >
          <template #prepend>
            <v-avatar
              :color="!job.avatar ? 'primary' : undefined"
              :class="!job.avatar && 'pt-1'"
              :image="job.avatar"
              icon="$vuetify"
            />
          </template>

          <template v-if="job.locations.length > 0" #subtitle>
            <div class="d-flex align-center">
              <v-icon
                class="me-1"
                icon="mdi-map-marker-outline"
                size="14"
              />

              {{ job.locations.join(', ') }}
            </div>
          </template>

          <template #append>
            <v-btn
              color="success"
              class="ms-6"
              size="small"
              style="pointer-events: none;"
              variant="flat"
            >
              {{ t('apply') }}

              <v-icon
                icon="mdi-open-in-new"
                end
                size="small"
              />
            </v-btn>
          </template>
        </v-list-item>

        <v-card-text
          class="text-medium-emphasis py-0"
          v-text="job.description"
        />

        <div class="text-end text-caption text-disabled mb-1 me-2">
          via {{ job.via }}
        </div>
      </v-card>
    </v-col>
  </v-row>
</template>

<script setup>
  // Composables
  import { useI18n } from 'vue-i18n'
  import { useJobsStore } from '@/store/jobs'

  // Utilities
  import { computed, ref } from 'vue'
  import { useGtag } from 'vue-gtag-next'

  const { event } = useGtag()
  const { jobs } = useJobsStore()
  const { t } = useI18n()
  const view = ref(true)
  const search = ref('')
  const items = computed(() => {
    return jobs.filter(job => {
      if (!search.value) return true

      const title = job.title.toLowerCase()
      const description = job.description.toLowerCase()
      const s = search.value.toLowerCase()

      return (title.includes(s) || description.includes(s))
    })
  })
  const placeholder = computed(() => {
    return t('search-jobs')
  })

  function onClick (job) {
    event('click', {
      event_category: 'vuetify-job',
      event_label: job.title,
      value: job.id,
    })
  }
</script>
