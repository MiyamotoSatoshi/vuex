<template>
  <component
    :is="component"
    v-if="component"
    v-bind="{
      ...$attrs,
      ...$props,
    }"
    v-on="$listeners"
  />
</template>

<script>
  // Utilities
  import { sync } from 'vuex-pathify'

  export default {
    name: 'VueFile',

    inheritAttrs: false,

    props: {
      file: {
        type: String,
        required: true,
      },
    },

    data: () => ({ component: undefined }),

    computed: { loading: sync('pages/loading') },

    created () {
      // Add pending promises to be resolved
      // before processing scrollBehavior.
      this.loading.push(new Promise(this.load))
    },

    methods: {
      async load (resolve, reject) {
        let component = {}

        try {
          component = await import(
            /* webpackChunkName: "examples" */
            /* webpackMode: "lazy-once" */
            `../../examples/${this.file}.vue`
          )

          this.$emit('loaded', component.default)
        } catch (err) {
          component = await import('./ExampleMissing')

          this.$emit('error', err)
        }

        this.component = component.default

        resolve()
      },
    },
  }
</script>
