<template>
  <v-container tag="section">
    <v-responsive
      class="mx-auto"
      max-width="768"
    >
      <component :is="component" />
    </v-responsive>
  </v-container>
</template>

<script>
  // Utilities
  import { genMetaData } from '@/util/metadata'
  import { sync, call } from 'vuex-pathify'

  // This should only be extended by other pages
  export default {
    name: 'Page',

    metaInfo () {
      // Check it fm exists
      if (!this.frontmatter) return {}

      // Check if meta exists
      const { meta } = this.frontmatter

      if (!meta) return

      const {
        description = '',
        keywords = '',
        title = '',
      } = meta

      return genMetaData(
        title,
        description,
        keywords,
      )
    },

    data: () => ({
      component: undefined,
    }),

    computed: {
      frontmatter: sync('i18n/frontmatter'),
    },

    watch: {
      '$route.params.locale': 'update',
    },

    created () {
      this.update()
    },

    methods: {
      init: call('page/init'),
      async load () { return {} },
      async update () {
        const fm = await this.load(this.$route)

        this.component = fm.default.vue.component
        this.frontmatter = fm.default.attributes

        this.init({ toc: fm.toc })
      },
    },
  }
</script>
